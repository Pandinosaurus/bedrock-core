import React from 'react';
import {
  Segment,
  Message,
  Form,
  Button,
  Divider,
  Header,
  Label,
} from 'semantic';

import { screen } from 'helpers';
import Menu from './Menu';
import { request } from 'utils/api';
// import SetupAuthenticator from 'modals/mfa/SetupAuthenticator';
//import SetupSMS from 'modals/mfa/SetupSMS';
//import ConfirmAccess from 'modals/ConfirmAccess';

import { withSession } from 'stores';
import { Link } from 'react-router-dom';
import { Layout } from 'components';

@screen
@withSession
export default class Account extends React.Component {
  state = {};

  componentDidMount() {
    if (
      Date.parse(this.context.user.accessConfirmedAt) <
      Date.now() - 30 * 60 * 1000
    ) {
      this.props.history.push(
        `/confirm-access?to=${this.props.location.pathname}`
      );
    }
  }

  render() {
    const { error } = this.state;
    const { mfaMethod } = this.context.user;

    return (
      <React.Fragment>
        <Menu />
        <Divider hidden />
        {error && <Message error content={error.message} />}

        <Header>Two-factor authentication</Header>
        <p>
          After logging, you will be required to enter a code using one of the
          methods below. You can always set up more than one method later.
        </p>
        <Divider hidden></Divider>

        <Segment.Group>
          <Segment>
            <Layout horizontal spread>
              <div>
                <Header
                  size="tiny"
                  style={{ marginTop: 0, marginBottom: '0.5em' }}>
                  App authentication{' '}
                  {mfaMethod === 'otp' && <Label color="green">Enabled</Label>}
                </Header>
                <p>
                  Security codes will be generated by your preferred
                  authenticator app.
                </p>
              </div>
              <div>
                <Button
                  basic
                  size="small"
                  to="/settings/mfa-authenticator"
                  as={Link}>
                  {mfaMethod === 'otp' ? 'Update' : 'Enable'}
                </Button>
              </div>
            </Layout>
          </Segment>
          <Segment>
            <Layout horizontal spread>
              <div>
                <Header
                  size="tiny"
                  style={{ marginTop: 0, marginBottom: '0.5em' }}>
                  SMS authentication
                  {mfaMethod == 'sms' && <Label color="green">Enabled</Label>}
                </Header>
                <p>
                  Security codes will be sent via SMS to your mobile device.
                </p>
              </div>
              <div>
                <Button basic size="small" to="/settings/mfa-sms" as={Link}>
                  {mfaMethod === 'sms' ? 'Update' : 'Enable'}
                </Button>
              </div>
            </Layout>
          </Segment>
        </Segment.Group>
        {mfaMethod && (
          <>
            <Divider hidden></Divider>

            <Segment color="red">
              <Layout horizontal spread>
                <div>
                  <Header
                    size="tiny"
                    style={{ marginTop: 0, marginBottom: '0.5em' }}>
                    Turn off two-factor authentication
                  </Header>
                  <p>
                    Turning off two-factor authentication will remove an extra
                    layer of security on your account.
                  </p>
                </div>
                <div>
                  <Button basic size="small" color="red">
                    Turn off
                  </Button>
                </div>
              </Layout>
            </Segment>
          </>
        )}
      </React.Fragment>
    );
  }
}
